package com.gk1;

public class Overloading {

	void show()
	{
		System.out.println("show()");
	}

	void show(int i)
	{
		System.out.println("show()--int i");
	}
	
	void show(int i,float k)
	{
		System.out.println("show()--int i");
	}
	
	void show(float k,int i)
	{
		System.out.println("show()--int i");
	}
}

